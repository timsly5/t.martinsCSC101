fn main() {
    let num1 = 5;
    let num2 = 12;
    let mut result:i32;

    result = num1 + num2;
    println!("Sum: {}",result);

    result = num1 - num2;
    println!("Difference: {}",result);

    result = num1*num2;
    println!("Product: {}",result);

    result = num1/num2;
    println!("Quotient: {}",result);

    result = num1%num2;
    println!("Remainder: {}",result);

}